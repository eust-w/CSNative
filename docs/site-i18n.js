let locale = normalizeLocale(localStorage.getItem(STORE_KEY) || "zh-CN");

function t(key) {
  return dictionaries[locale]?.[key] ?? dictionaries.en?.[key] ?? zh[key] ?? key;
}

function normalizeLocale(next) {
  return LOCALES.some((item) => item.id === next) ? next : "zh-CN";
}

function applyDocumentLocale() {
  document.documentElement.lang = locale;
  document.documentElement.dir = RTL.has(locale) ? "rtl" : "ltr";
}

function fillLocaleSelect(select) {
  select.innerHTML = "";
  for (const item of LOCALES) {
    const option = document.createElement("option");
    option.value = item.id;
    option.textContent = item.name;
    select.appendChild(option);
  }
  select.value = locale;
}

function applyI18n(root = document) {
  applyDocumentLocale();
  document.title = t("meta.title");
  document.querySelector('meta[name="description"]')?.setAttribute("content", t("meta.description"));
  document.querySelector('meta[property="og:description"]')?.setAttribute("content", t("meta.ogDescription"));
  root.querySelectorAll("[data-i18n]").forEach((node) => {
    node.textContent = t(node.dataset.i18n);
  });
  root.querySelectorAll("[data-i18n-html]").forEach((node) => {
    node.innerHTML = t(node.dataset.i18nHtml);
  });
  [
    ["data-i18n-aria-label", "aria-label"],
    ["data-i18n-alt", "alt"],
  ].forEach(([dataAttr, attr]) => {
    root.querySelectorAll(`[${dataAttr}]`).forEach((node) => {
      node.setAttribute(attr, t(node.getAttribute(dataAttr)));
    });
  });
}

window.addEventListener("DOMContentLoaded", () => {
  const select = document.getElementById("siteLocale");
  fillLocaleSelect(select);
  applyI18n(document);
  select.addEventListener("change", () => {
    locale = normalizeLocale(select.value);
    localStorage.setItem(STORE_KEY, locale);
    applyI18n(document);
    select.value = locale;
  });
});
